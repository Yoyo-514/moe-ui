import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const currentDir = dirname(fileURLToPath(import.meta.url))
const pkgRoot = resolve(currentDir, '..')
const componentsRoot = resolve(pkgRoot, '../components')
const componentsEntry = resolve(componentsRoot, 'index.ts')
const target = resolve(pkgRoot, 'components.ts')

const entryContent = readFileSync(componentsEntry, 'utf-8')
const componentDirs = [...entryContent.matchAll(/export \* from '\.\/(.+)'/g)]
  .map(([, name]) => name)
  .sort((a, b) => a.localeCompare(b))

const pluginNames = componentDirs.flatMap((dir) => {
  const content = readFileSync(resolve(componentsRoot, dir, 'index.ts'), 'utf-8')
  const constExports = [...content.matchAll(/export const (Moe[A-Za-z0-9]+)/g)].map(
    ([, name]) => name
  )
  const namedExports = [...content.matchAll(/export \{([^}]+)\}/g)]
    .flatMap(([, names]) => names.split(','))
    .map((name) => name.trim())
    .filter((name) => /^Moe[A-Za-z0-9]+$/.test(name))

  return [...constExports, ...namedExports]
})

const uniquePluginNames = [...new Set(pluginNames)]

const importBlock = uniquePluginNames.map((name) => `  ${name},`).join('\n')
const arrayBlock = uniquePluginNames.map((name) => `  ${name},`).join('\n')

const output = `// This file is generated by packages/core/build/generate-components.mjs.\n// Do not edit manually.\n\nimport {\n${importBlock}\n} from '@moe-ui/components'\nimport type { Plugin } from 'vue'\n\nexport default [\n${arrayBlock}\n] as Plugin[]\n`

writeFileSync(target, output, 'utf-8')
