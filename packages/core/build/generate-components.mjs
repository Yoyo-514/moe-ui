import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const currentDir = dirname(fileURLToPath(import.meta.url))
const pkgRoot = resolve(currentDir, '..')
const componentsRoot = resolve(pkgRoot, '../components')
const componentsEntry = resolve(componentsRoot, 'index.ts')
const componentTarget = resolve(pkgRoot, 'component.ts')
const resolverTarget = resolve(pkgRoot, 'resolver.ts')

const entryContent = readFileSync(componentsEntry, 'utf-8')
const componentDirs = [...entryContent.matchAll(/export \* from '\.\/(.+)'/g)]
  .map(([, name]) => name)
  .sort((a, b) => a.localeCompare(b))

function getMoeExports(dir) {
  const content = readFileSync(resolve(componentsRoot, dir, 'index.ts'), 'utf-8')
  const constExports = [...content.matchAll(/export const (Moe[A-Za-z0-9]+)/g)].map(
    ([, name]) => name
  )
  const namedExports = [...content.matchAll(/export \{([^}]+)\}/g)]
    .flatMap(([, names]) => names.split(','))
    .map((name) => name.trim())
    .filter((name) => /^Moe[A-Za-z0-9]+$/.test(name))

  return [...constExports, ...namedExports]
}

const componentEntries = componentDirs.flatMap((dir) =>
  getMoeExports(dir).map((name) => [name, dir])
)
const uniqueComponentEntries = [...new Map(componentEntries).entries()]
const pluginNames = uniqueComponentEntries.map(([name]) => name)

const importBlock = pluginNames.map((name) => `  ${name},`).join('\n')
const arrayBlock = pluginNames.map((name) => `  ${name},`).join('\n')

const componentOutput = `// This file is generated by packages/core/build/generate-components.mjs.\n// Do not edit manually.\n\nimport type { Plugin } from 'vue'\n\nimport {\n${importBlock}\n} from '@moe-ui/components'\n\nexport default [\n${arrayBlock}\n] as Plugin[]\n`

const resolverMapBlock = uniqueComponentEntries
  .map(([name, dir]) => `  ${name}: '${dir}',`)
  .join('\n')

const resolverOutput = `// This file is generated by packages/core/build/generate-components.mjs.\n// Do not edit manually.\n\nexport interface MoeUIResolverOptions {\n  /**\n   * Import component styles automatically.\n   *\n   * @default 'css'\n   */\n  importStyle?: boolean | 'css'\n}\n\nexport interface MoeUIResolvedImport {\n  name: string\n  from: string\n  sideEffects?: string\n}\n\nconst PACKAGE_NAME = 'moe-cute-ui'\n\nconst componentEntryMap: Record<string, string> = {\n${resolverMapBlock}\n}\n\nfunction resolveEntryName(name: string) {\n  return componentEntryMap[name]\n}\n\nfunction getSideEffects(entryName: string, importStyle: MoeUIResolverOptions['importStyle']) {\n  if (!importStyle) return undefined\n\n  return \`${'${PACKAGE_NAME}'}/es/components/${'${entryName}'}/style/css\`\n}\n\nexport function MoeUIResolver(options: MoeUIResolverOptions = {}) {\n  const { importStyle = 'css' } = options\n\n  return {\n    resolve(name: string): MoeUIResolvedImport | undefined {\n      const entryName = resolveEntryName(name)\n\n      if (!entryName) return undefined\n\n      return {\n        name,\n        from: \`${'${PACKAGE_NAME}'}/es/components/${'${entryName}'}\`,\n        sideEffects: getSideEffects(entryName, importStyle),\n      }\n    },\n  }\n}\n`

writeFileSync(componentTarget, componentOutput, 'utf-8')
writeFileSync(resolverTarget, resolverOutput, 'utf-8')
